import mongoose from 'mongoose';

const statementSchema = new mongoose.Schema(
  {
    fileName: {
      type: String,
      required: true,
    },
    startDate: {
      type: Date,
      required: true,
    },
    endDate: {
      type: Date,
      required: true,
    },
    uploadedBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Statement = mongoose.model('Statement', statementSchema);

export default Statement;
